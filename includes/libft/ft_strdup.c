#include "libft.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*str;

	if (!src)
		return (NULL);
	i = ft_strlen(src);
	if ((str = (char *)malloc((i + 1) * sizeof(*str))) == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
